# Crie um script em Ruby que receba dois números, X e Y e retorne o resto da divisão entre eles.

puts "Digite o primeiro número a ser calculado:"
n1 = gets.chomp.to_i

puts "Digite o segundo número a ser calculado:"
n2 = gets.chomp.to_i


resto = n1 % n2

puts "O resto da divisão deu #{resto}"






